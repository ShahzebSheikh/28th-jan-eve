package control_statement;

public class _switch {

public static void main(String[] args) {
	int s2=5;
	switch(s2)
	{
	case 1:System.out.println("1st class");
	break;
	case 2:System.out.println("2nd class");
	break;
	case 3:System.out.println("3rd class");
	break;
	case 4:System.out.println("4th class");
	break;
	case 5:System.out.println("5th class");
	break;
	case 6:System.out.println("6th class");
	break;
	default:System.out.println("no class");
	
	}
	
	
	
	
}




}